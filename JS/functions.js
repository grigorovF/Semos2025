//fuctions

function adding(a, b){
    return a+b;
}

function substracting(a, b){
    return a-b;
}

function multiplication(a, b){
    return a*b;
}

function devide(a, b){
    return (a/b)
}

function modulation(a, b){
    return a%b
}

var a = 6;
var b = 3;

console.log("6 + 3 = ", adding(a, b))
console.log("6 - 3 = ", substracting(a, b))
console.log("6 * 3 = ", multiplication(a, b))
console.log("6 / 3 = ", devide(a, b))
console.log("6 % 3 = ", modulation(a, b))



